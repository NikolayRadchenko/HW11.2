def _upper(string):
    """
        Преобразовывает строку в строку со всеми заглавными буквами
        :param string: строка
        :return: строка с заглавными буквами
        """
    return string.upper()


def _title(string):
    """
    Преобразовывает строку в строку с первыми заглавными буквами каждого слова
    :param string: строка для преобразования
    :return: возвразает строку в преобразованном виде
    """
    return string.title()
